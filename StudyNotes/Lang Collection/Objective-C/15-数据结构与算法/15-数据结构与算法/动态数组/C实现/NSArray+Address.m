#import "NSArray+Address.h"

@implementation NSArray (Address)

- (void *)elementsAddress
{
    /*
     (lldb) x/80xb array
     0x10060b590: 0x31 0x37 0x4b 0x88 0xff 0xff 0x1d 0x02
     0x10060b598: 0x00 0x00 0x00 0x00 0x00 0x00 0x00 0x00
     0x10060b5a0: 0xe0 0xab 0x60 0x00 0x01 0x00 0x00 0x00
     (lldb) x/100xb 0x010060abe0
     0x10060abe0: 0x48 0x41 0x00 0x00 0x01 0x00 0x00 0x00
     0x10060abe8: 0x00 0x00 0x00 0x00 0x00 0x00 0x00 0x00
     (lldb) x/100xb 0x010060abe0
     0x10060abe0: 0x48 0x41 0x00 0x00 0x01 0x00 0x00 0x00
     0x10060abe8: 0x48 0x41 0x00 0x00 0x01 0x00 0x00 0x00
     */
    void *address = (__bridge void *)(self);
    return *((void **)address + 2);
}

@end
